// an editable cell renderer
const EditableCell = ({
  value: initialValue,
  row: { index },
  column: { id },
  updateMyData, // a custom function that we supplied to table instance
}) => {
  const [value, setValue] = React.useState(initialValue);

  const onChange = (e) => {
    setValue(e.target.value);
  };

  React.useEffect(() => {
    setValue(initialValue);
  }, [initialValue]);

  if (id === "label") {
    return (
      <Autocomplete
        getItemValue={(item) => item.label}
        items={top100Films}
        renderItem={(item, isHighlighted) => {
          if (item.label.toLowerCase().includes(value.toLowerCase())) {
            return (
              <div
                key={`${item.label}${item.year}`}
                style={{ background: isHighlighted ? "lightgray" : "white" }}
              >
                {item.label}
              </div>
            );
          } else return <div key={`${item.label}${item.year}`}>{null}</div>;
        }}
        value={value}
        onChange={onChange}
        onSelect={(val, el) => {
          updateMyData(index, id, el);
        }}
      />
    );
  } else if (id === "year") {
    return <div>{value}</div>;
  }
};

const defaultColumn = {
  Cell: EditableCell,
};

function TableComponent({ columns, data, updateMyData, skipPageReset }) {
  const {
    getTableProps,
    getTableBodyProps,
    headerGroups,
    prepareRow,
    page,
    canPreviousPage,
    canNextPage,
    pageOptions,
    pageCount,
    gotoPage,
    nextPage,
    previousPage,
    setPageSize,
    state: { pageIndex, pageSize },
  } = useTable(
    {
      columns,
      data,
      defaultColumn,
      autoResetPage: !skipPageReset,
      updateMyData,
    },
    usePagination
  );

  // Render the UI for your table
  return (
    <>
      <table {...getTableProps()}>
        <thead>
          {headerGroups.map((headerGroup) => (
            <tr {...headerGroup.getHeaderGroupProps()}>
              {headerGroup.headers.map((column) => (
                <th {...column.getHeaderProps()}>{column.render("Header")}</th>
              ))}
            </tr>
          ))}
        </thead>
        <tbody {...getTableBodyProps()}>
          {page.map((row, i) => {
            prepareRow(row);
            return (
              <tr {...row.getRowProps()}>
                {row.cells.map((cell) => {
                  return (
                    <td {...cell.getCellProps()}>{cell.render("Cell")}</td>
                  );
                })}
              </tr>
            );
          })}
        </tbody>
      </table>
      <div className="pagination">
        <button onClick={() => gotoPage(0)} disabled={!canPreviousPage}>
          {"<<"}
        </button>{" "}
        <button onClick={() => previousPage()} disabled={!canPreviousPage}>
          {"<"}
        </button>{" "}
        <button onClick={() => nextPage()} disabled={!canNextPage}>
          {">"}
        </button>{" "}
        <button onClick={() => gotoPage(pageCount - 1)} disabled={!canNextPage}>
          {">>"}
        </button>{" "}
        <span>
          Page{" "}
          <strong>
            {pageIndex + 1} of {pageOptions.length}
          </strong>{" "}
        </span>
        <span>
          | Go to page:{" "}
          <input
            type="number"
            defaultValue={pageIndex + 1}
            onChange={(e) => {
              const page = e.target.value ? Number(e.target.value) - 1 : 0;
              gotoPage(page);
            }}
            style={{ width: "100px" }}
          />
        </span>{" "}
        <select
          value={pageSize}
          onChange={(e) => {
            setPageSize(Number(e.target.value));
          }}
        >
          {[10, 20, 30, 40, 50].map((pageSize) => (
            <option key={pageSize} value={pageSize}>
              Show {pageSize}
            </option>
          ))}
        </select>
      </div>
    </>
  );
}

const Table = () => {
  const columns = React.useMemo(
    () => [
      {
        Header: "Name",
        accessor: "label",
      },
      {
        Header: "Year",
        accessor: "year",
      },
    ],
    []
  );

  const [data, setData] = React.useState(() => [{ label: "", year: "" }]);
  const [skipPageReset, setSkipPageReset] = React.useState(false);

  const updateMyData = (rowIndex, columnId, el) => {
    // turn on the flag to not reset the page
    setSkipPageReset(true);
    let currentData = data;
    currentData[rowIndex] = el;
    if (rowIndex === data.length - 1) {
      setData([...currentData, { label: "", year: "" }]);
    } else {
      setData([...currentData]);
    }
  };

  React.useEffect(() => {
    setSkipPageReset(false);
  }, [data]);

  return (
    <Styles>
      <TableComponent
        columns={columns}
        data={data}
        updateMyData={updateMyData}
        skipPageReset={skipPageReset}
      />
      <button onClick={() => console.log(data.slice(0, data.length - 1))}>
        show data
      </button>
    </Styles>
  );
};

export default Table;
